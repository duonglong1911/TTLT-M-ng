    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
//         TODO add your handling code here:
        String ma = txt_ma.getText();
        String ten = txt_ten.getText();
        float soluong = Float.parseFloat(txt_soluong.getText());
        float gia = Float.parseFloat(txt_gia.getText());
        product = new Product(ma, ten, soluong, gia);
        productList.add(product);
        DefaultTableModel model = (DefaultTableModel) tbl_sanpham.getModel();
        model.getDataVector().removeAllElements();
        for (Product product1 : productList) {
            System.out.println(product1.getTenHH());
            Object[] row = { product1.tenHH,product1.maHH,product1.soLuong,product1.gia1SP};
            model.addRow(row);
        }

        
    }//GEN-LAST:event_btn_themActionPerformed

